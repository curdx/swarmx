/// New ad-hoc worker (insert payload for [`crate::Store::record_worker`]).
/// Magentic-One йҮҚжһ„еҗҺдёҡеҠЎ agent дёҚеҶҚжқҘиҮӘ spell+role,иҖҢжҳҜ orchestrator еңЁ
/// runtime з”ЁиҮӘе®ҡд№ү prompt жӢүзҡ„дёҙж—¶е·ҘгҖӮиҝҷеј иЎЁжҠҠ"и°ҒжӢүзҡ„гҖҒд»Җд№Ҳжҙ»гҖҒзӯүе•ҘдҝЎеҸ·"
/// и®°дёӢжқҘ,DAG / wake-coordinator / еӣһж”ҫйғҪйқ е®ғгҖӮ
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct NewWorker {
    /// PTY agent_id(еҗҢж—¶жҳҜ agents иЎЁдё»й”®)гҖӮ
    pub agent_id: String,
    /// жӢүиө·иҝҷдёӘ worker зҡ„ orchestrator(жҲ–дёҠдёҖзә§ worker)зҡ„ agent_idгҖӮ
    pub parent_agent_id: String,
    /// д»… UI жҳҫзӨәз”Ёзҡ„ role еҗҚеӯ—,дҫӢеҰӮ "writer" / "ui-coder" / "test-runner"гҖӮ
    pub role_label: String,
    /// жіЁе…Ҙз»ҷ worker зҡ„е®Ңж•ҙ system prompt,з•ҷжЎЈдҫҝдәҺеӣһж”ҫгҖӮ
    pub system_prompt: String,
    /// е®ҢжҲҗеҗҺиҜҘеҶҷзҡ„й»‘жқҝ key;ж—  handoff з•ҷз©әеӯ—з¬ҰдёІгҖӮP0-A иө·з”ұжңҚеҠЎз«Ҝ mint
    /// (`<workspace_id>/<thread_slug>/<role>.<kind>`),дёҚеҶҚжҳҜ LLM иҮӘйҖүдёІгҖӮ
    pub handoff_signal: String,
    /// JSON еәҸеҲ—еҢ–зҡ„ string array,зӯүеҫ…зҡ„й»‘жқҝ keyгҖӮз©әж•°з»„ = ж— дҫқиө–гҖӮP0-A иө·
    /// з”ұ `consumes_json` и§ЈжһҗжҲҗзҡ„ minted keys еЎ«е…ҘгҖӮ
    pub depends_on_json: String,
    pub spawned_at: i64,

    // в”Җв”Җ P0 (F1 и§’иүІ/д»»еҠЎж„ҹзҹҘй…Қз»ҷ) в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
    /// ж ЎйӘҢиҝҮзҡ„и§’иүІжіЁеҶҢиЎЁ slug(P0-B)гҖӮ`role_label` дҝқз•ҷдҪң UI жҳҫзӨә(з”ұ slug жҙҫз”ҹ)гҖӮ
    #[serde(default)]
    pub role_slug: String,
    /// JSON ж•°з»„:жң¬ worker дә§еҮәзҡ„ typed output-kinds(P0-A)гҖӮз©ә = ["done"]гҖӮ
    #[serde(default)]
    pub produces_json: String,
    /// JSON ж•°з»„:{from_role, kind} typed дёҠжёёдҫқиө–(P0-A)гҖӮи§ЈжһҗеҗҺзҡ„ minted key
    /// иҗҪеңЁ `depends_on_json`гҖӮ
    #[serde(default)]
    pub consumes_json: String,
}

